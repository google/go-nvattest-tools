// gpuOpaqueDataParser is a parser for GPU opaque data.
type gpuOpaqueDataParser struct{}

// ParseOpaqueData parses the given byte slice and returns the OpaqueData.
func (g *gpuOpaqueDataParser) ParseOpaqueData(b []uint8, opaqueLength int) (od *pb.OpaqueData, err error) {
	defer func() {
		if info := recover(); info != nil {
			od = nil
			err = &ParsingError{
				Context: "parseOpaqueData(...)",
				Info:    OutOfRangeRuntimeError,
			}
		}
	}()

	if len(b) < opaqueLength {
		return nil, &IncorrectLengthError{
			Context:  "opaque data",
			Expected: opaqueLength,
			Actual:   len(b),
		}
	}

	data := clone(b[:opaqueLength]) // Created an independent copy to make the interface less error-prone
	offset := 0
	od = &pb.OpaqueData{}

	for offset < opaqueLength {
		rawDataType := binary.LittleEndian.Uint16(data[offset : offset+OpaqueDataTypeFieldSize])
		offset += OpaqueDataTypeFieldSize

		dataSize := int(binary.LittleEndian.Uint16(data[offset : offset+OpaqueDataSizeFieldSize]))
		offset += OpaqueDataSizeFieldSize

		value := data[offset : offset+dataSize]
		offset += dataSize

		dataType := pb.OpaqueDataType_OPAQUE_FIELD_ID_UNSPECIFIED
		if odt, ok := gpuOpaqueDataTypes[rawDataType]; ok {
			dataType = odt
		}
		ofd := &pb.OpaqueFieldData{DataType: dataType}

		if dataType == pb.OpaqueDataType_OPAQUE_FIELD_ID_MSRSCNT {
			msrCounts, err := parseMeasurementCounts(value)
			if err != nil {
				return nil, err
			}
			ofd.Data = &pb.OpaqueFieldData_MsrCounts{
				MsrCounts: msrCounts,
			}
		} else if dataType == pb.OpaqueDataType_OPAQUE_FIELD_ID_SWITCH_PDI {
			switchPdis, err := g.parseSwitchPdis(value)
			if err != nil {
				return nil, err
			}
			ofd.Data = &pb.OpaqueFieldData_SwitchPdis{
				SwitchPdis: switchPdis,
			}
		} else {
			ofd.Data = &pb.OpaqueFieldData_Value{
				Value: value,
			}
		}
		od.OpaqueFieldData = append(od.OpaqueFieldData, ofd)
	}

	return od, nil
}

func (g *gpuOpaqueDataParser) parseSwitchPdis(b []uint8) (*pb.SwitchPdis, error) {
	if len(b)%OpaquePdiDataSizeFieldSize != 0 {
		return nil, &InvalidSizeSwitchPdisError{
			Expected: OpaquePdiDataSizeFieldSize,
			Actual:   len(b),
		}
	}

	data := clone(b) // Created an independent copy to make the interface less error-prone
	offset := 0
	switchPdis := &pb.SwitchPdis{}

	for offset < len(data) {
		value := data[offset : offset+OpaquePdiDataSizeFieldSize]
		switchPdis.Values = append(switchPdis.Values, value)
		offset += OpaquePdiDataSizeFieldSize
	}
	return switchPdis, nil
}

