// AttestationType represents the type of attestation.
type AttestationType int

// Attestation types.
const (
	// GPU attestation type.
	GPU AttestationType = iota
	// SWITCH attestation type.
	SWITCH
)

func (at AttestationType) String() string {
	switch at {
	case GPU:
		return "GPU"
	case SWITCH:
		return "SWITCH"
	default:
		return fmt.Sprintf("Unknown AttestationType(%d)", at)
	}
}

// RawAttestationReportToProto parses a byte slice and converts it into an AttestationReport proto.
func RawAttestationReportToProto(b []byte, at AttestationType) (*pb.AttestationReport, error) {
	// The nonce set by a client is part of the SPDM measurement request body, whereas the nonce in
	// the SPDM measurement response body is a random value generated by a GPU or Switch device.
	// Hence, we need to have SPDM request as part of the attestation report message.
	spdmMeasurementRequest, err := parseSpdmMeasurementRequest(b)
	if err != nil {
		return nil, err
	}
	var spdmMeasurementResponse *pb.SpdmMeasurementResponse
	switch at {
	case GPU:
		spdmMeasurementResponse, _, err = parseSpdmMeasurementResponse(b[SpdmRequestSize:], &gpuOpaqueDataParser{}, GpuAttestationReportSignatureFieldSize)
	case SWITCH:
		spdmMeasurementResponse, _, err = parseSpdmMeasurementResponse(b[SpdmRequestSize:], &switchOpaqueDataParser{}, SwitchAttestationReportSignatureFieldSize)
	default:
		return nil, &UnsupportedAttestationTypeError{AttestationType: at}
	}

	if err != nil {
		return nil, err
	}
	return &pb.AttestationReport{
		SpdmMeasurementRequest:  spdmMeasurementRequest,
		SpdmMeasurementResponse: spdmMeasurementResponse,
	}, nil
}

/*
	 The SPDM measurement request is defined as follows:
		FIELD_NAME (FIELD_SIZE bytes) -- FIELD_NAME is the name of the field. FIELD_SIZE is the size of the field in bytes.
		- SPDM Version (SpdmVersionFieldSize)
		- Request/Response Code (RequestResponseCodeFieldSize)
		- Param1 (Param1FieldSize)
		- Param2 (Param2FieldSize)
		- Nonce (NonceFieldSize)
		- Slot ID Param (SlotIDParamFieldSize)
*/
func parseSpdmMeasurementRequest(b []uint8) (*pb.SpdmMeasurementRequest, error) {
	if len(b) < SpdmRequestSize {
		return nil, &IncorrectLengthError{
			Context:  "raw attestation report SPDM measurement request size",
			Expected: SpdmRequestSize,
			Actual:   len(b),
		}
	}

	data := clone(b[:SpdmRequestSize]) // Created an independent copy to make the interface less error-prone

	offset := 0

	spdmVersion := data[offset : offset+SpdmVersionFieldSize]
	offset += SpdmVersionFieldSize

	requestResponseCode := data[offset : offset+RequestResponseCodeFieldSize]
	offset += RequestResponseCodeFieldSize

	param1 := data[offset : offset+Param1FieldSize]
	offset += Param1FieldSize

	param2 := data[offset : offset+Param2FieldSize]
	offset += Param2FieldSize

	nonce := data[offset : offset+NonceFieldSize]
	offset += NonceFieldSize

	slotIDParam := data[offset : offset+SlotIDParamFieldSize]
	offset += SlotIDParamFieldSize

	if offset != SpdmRequestSize {
		return nil, &ParsingError{
			Context: "parseSpdmMeasurementRequest(...)",
			Info:    fmt.Sprintf("something went wrong while parsing SPDM measurement request. SPDM measurement request bytes length is %d bytes, expected %d bytes", offset, SpdmRequestSize),
		}
	}

	return &pb.SpdmMeasurementRequest{SpdmVersion: spdmVersion, RequestResponseCode: requestResponseCode, Param1: param1, Param2: param2, Nonce: nonce, SlotIdParam: slotIDParam}, nil
}

/*
	 The SPDM measurement response is defined as follows:
		FIELD_NAME (FIELD_SIZE bytes) -- FIELD_NAME is the name of the field. FIELD_SIZE is the size of the field in bytes.
		- SPDM Version (SpdmVersionFieldSize)
		- Request/Response Code (RequestResponseCodeFieldSize)
		- Param1 (Param1FieldSize)
		- Param2 (Param2FieldSize)
		- Number of Blocks (NumberOfBlocksFieldSize)
		- Measurement Record Length (MeasurementRecordLengthFieldSize)
		- Measurement Record (measurementRecordLength)
		- Nonce (NonceFieldSize)
		- Opaque Length (OpaqueLengthFieldSize)
		- Opaque Data (opaqueLength)
		- Signature (signatureLength)
*/
func parseSpdmMeasurementResponse(b []uint8, opaqueDataParser opaqueDataParser, signatureLength int) (response *pb.SpdmMeasurementResponse, parsedLength int, err error) {
	defer func() {
		if info := recover(); info != nil {
			response = nil
			err = &ParsingError{
				Context: "parseSpdmMeasurementResponse(...)",
				Info:    OutOfRangeRuntimeError,
			}
		}
	}()

	data := clone(b) // Created an independent copy to make the interface less error-prone

	offset := 0

	spdmVersion := data[offset : offset+SpdmVersionFieldSize]
	offset += SpdmVersionFieldSize

	requestResponseCode := data[offset : offset+RequestResponseCodeFieldSize]
	offset += RequestResponseCodeFieldSize

	param1 := data[offset : offset+Param1FieldSize]
	offset += Param1FieldSize

	param2 := data[offset : offset+Param2FieldSize]
	offset += Param2FieldSize

	numberOfBlocks := int(data[offset])
	offset += NumberOfBlocksFieldSize

	measurementRecordLength := int(binary.LittleEndian.Uint32([]byte{data[offset], data[offset+1], data[offset+2], 0}))
	offset += MeasurementRecordLengthFieldSize

	measurementRecord, err := parseMeasurementRecord(data[offset:], numberOfBlocks, measurementRecordLength)
	if err != nil {
		return nil, offset, err
	}
	offset += measurementRecordLength

	nonce := data[offset : offset+NonceFieldSize]
	offset += NonceFieldSize

	opaqueLength := int32(binary.LittleEndian.Uint16(data[offset : offset+OpaqueLengthFieldSize]))
	offset += OpaqueLengthFieldSize

	opaqueData, err := opaqueDataParser.ParseOpaqueData(data[offset:], int(opaqueLength))
	if err != nil {
		return nil, offset, err
	}
	offset += int(opaqueLength)

	signature, err := parseSignature(data[offset:], signatureLength)
	offset += signatureLength
	if err != nil {
		return nil, offset, err
	}

	return &pb.SpdmMeasurementResponse{
		SpdmVersion:         spdmVersion,
		RequestResponseCode: requestResponseCode,
		Param1:              param1,
		Param2:              param2,
		Nonce:               nonce,
		MeasurementRecord:   measurementRecord,
		OpaqueLength:        opaqueLength,
		OpaqueData:          opaqueData,
		Signature:           signature,
	}, offset, nil
}

func parseMeasurementRecord(b []uint8, numberOfBlocks int, measurementRecordLength int) (measurementRecord *pb.MeasurementRecord, err error) {
	defer func() {
		if info := recover(); info != nil {
			measurementRecord = nil
			err = &ParsingError{
				Context: "parseMeasurementRecord(...)",
				Info:    OutOfRangeRuntimeError,
			}
		}
	}()

	if len(b) < measurementRecordLength {
		return nil, &IncorrectLengthError{
			Context:  "measurement record",
			Expected: measurementRecordLength,
			Actual:   len(b),
		}
	}

	if numberOfBlocks <= 0 {
		return nil, &NoMeasurementBlocksError{}
	}

	data := clone(b[:measurementRecordLength]) // Created an independent copy to make the interface less error-prone
	offset := 0
	measurementRecord = &pb.MeasurementRecord{}

	for j := 0; j < numberOfBlocks; j++ {
		index := uint8(data[offset])
		offset += MeasurementBlockIndexFieldSize

		measurementSpecification := data[offset]
		offset += MeasurementBlockSpecificationFieldSize
		if measurementSpecification != DmtfMeasurementSpecificationValue {
			return nil, &UnsupportedDmtfMeasurementSpecificationError{
				Index: index,
			}
		}

		measurementSize := int(binary.LittleEndian.Uint16(data[offset : offset+MeasurementBlockSizeFieldSize]))
		offset += MeasurementBlockSizeFieldSize

		dmtfMeasurement, _, err := parseDmtfMeasurement(data[offset:], measurementSize)
		if err != nil {
			return nil, err
		}

		measurementBlock := &pb.MeasurementBlock{Index: int32(index), Specification: int32(measurementSpecification), DmtfMeasurement: dmtfMeasurement}
		offset += measurementSize

		measurementRecord.MeasurementBlocks = append(measurementRecord.MeasurementBlocks, measurementBlock)
	}

	if offset != measurementRecordLength {
		return nil, &ParsingError{
			Context: "parseMeasurementRecord(...)",
			Info:    fmt.Sprintf("something went wrong while parsing measurement record. Measurement record bytes length is %d bytes, expected %d bytes", offset, measurementRecordLength),
		}
	}

	return measurementRecord, nil
}

func parseDmtfMeasurement(b []uint8, measurementSize int) (dmtfMeasurement *pb.DmtfMeasurement, parsedLength int, err error) {
	defer func() {
		if info := recover(); info != nil {
			dmtfMeasurement = nil
			err = &ParsingError{
				Context: "parseDmtfMeasurement(...)",
				Info:    OutOfRangeRuntimeError,
			}
		}
	}()

	if len(b) < measurementSize {
		return nil, 0, &IncorrectLengthError{
			Context:  "dmtf measurement",
			Expected: measurementSize,
			Actual:   len(b),
		}
	}

	data := clone(b[:measurementSize]) // Created an independent copy to make the interface less error-prone
	offset := 0

	valueType := data[offset : offset+DmtfSpecMeasurementValueTypeFieldSize]
	offset += DmtfSpecMeasurementValueTypeFieldSize

	valueSize := binary.LittleEndian.Uint16(data[offset : offset+DmtfSpecMeasurementValueSizeFieldSize])
	offset += DmtfSpecMeasurementValueSizeFieldSize

	value := data[offset : offset+int(valueSize)]
	offset += int(valueSize)

	return &pb.DmtfMeasurement{ValueType: valueType, ValueSize: int32(valueSize), Value: value}, offset, nil
}

func parseSignature(b []uint8, signatureLength int) ([]byte, error) {
	if len(b) < signatureLength {
		return nil, &IncorrectLengthError{
			Context:  "signature",
			Expected: signatureLength,
			Actual:   len(b),
		}
	}
	data := clone(b[:signatureLength]) // Created an independent copy to make the interface less error-prone
	return data, nil
}

func parseMeasurementCounts(b []uint8) (*pb.MsrCounts, error) {
	if len(b)%OpaqueDataMsrCountSize != 0 {
		return nil, &InvalidSizeMeasurementCountsError{
			Expected: OpaqueDataMsrCountSize,
			Actual:   len(b),
		}
	}

	data := clone(b) // Created an independent copy to make the interface less error-prone
	msrCounts := &pb.MsrCounts{}

	for i := 0; i < len(data)/OpaqueDataMsrCountSize; i++ {
		start := i * OpaqueDataMsrCountSize
		end := start + OpaqueDataMsrCountSize
		value := binary.LittleEndian.Uint32(data[start:end])
		msrCounts.Values = append(msrCounts.Values, int32(value))
	}
	return msrCounts, nil
}

